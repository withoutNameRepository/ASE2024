def sum_of_divisors_in_range(n):
    if n < 60:
        return 0
    elif n < 65:
        return n - 60
    elif n < 70:
        return n - 65
    elif n < 75:
        return n - 70
    elif n < 80:
        return n - 75
    elif n < 85:
        return n - 80
    elif n < 90:
        return n - 85
    else:
        return n - 90
