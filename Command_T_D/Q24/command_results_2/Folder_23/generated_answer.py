def sum_of_divisors_in_range(n):
    if n <= 18:
        return 0
    if n <= 22:
        return 1
    elif n <= 26:
        return 2
    elif n <= 30:
        return 3
    elif n <= 36:
        return 4
    elif n <= 42:
        return 5
    elif n <= 46:
        return 6
    elif n <= 50:
        return 7
    else:
        return 8
