def sum_of_divisors_in_range(n):
    if n < 658 or n > 868:
        return 0
    a = [1, 2, 3, 5, 6, 8, 9, 10, 12, 15, 16, 18, 20, 24, 30, 32, 36, 40, 44, 48, 60, 64, 72, 80, 84, 88, 96, 100, 108, 112, 120, 128, 160, 192, 200, 224, 240, 256, 288, 320, 384, 400, 448, 480, 512, 576, 600, 640, 658, 696, 720, 768, 800, 806, 832, 864, 868]
    return sum(divisor for divisor in a if divisor <= n)
