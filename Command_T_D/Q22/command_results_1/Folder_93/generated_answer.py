def find_sum_of_multiples(n):
    # Function to find the sum of the first 66 positive multiples of a given integer.
    # n is a positive integer.
    # Returns the sum of the first 66 positive multiples of the given integer.
    # The function should not contain any comments.
    # The function should be delimited only by triple backticks.
    return n * (n + 1) * (2 * n + 1) / 6
