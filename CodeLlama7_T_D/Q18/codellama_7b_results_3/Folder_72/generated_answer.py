
def sum_ints_div_by_either_nums(my_list):
    # Initialize the sum to 0
    sum = 0

    # Iterate through the list, starting from index 38 and ending at index 81 (inclusive)
    for i in range(38, 82):
        # If the current element is divisible by either -61 or -71, add it to the sum
        if my_list[i] % (-61 or -71) == 0:
            sum += my_list[i]

    # Return the sum

    return sum
