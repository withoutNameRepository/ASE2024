
def is_prime(n):
    if n <= 1:
        return False
    if n == 2:
        return True
    if n % 2 == 0:
        return False
    i = 3
    while i * i <= n:
        if n % i:
            i += 2
        else:
            return False
    return True

def find_primes_between_indices(lst):
    return sorted([i for i in lst[66:82] if is_prime(i)])
