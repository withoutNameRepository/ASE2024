
def prime_factors(lst):
    num = lst[162]
    def prime_helper(n):
        i = 2
        factors = set()
        while i * i <= n:
            if n % i:
                i += 1
            else:
                n //= i
                factors.add(i)
        if n > 1:
            factors.add(n)
        return factors
    return prime_helper(num)
